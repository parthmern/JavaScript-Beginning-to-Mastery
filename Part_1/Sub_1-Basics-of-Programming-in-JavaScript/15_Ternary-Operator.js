// ternary operator / conditional operator 
//-------------------------------------
let age = 38 ;

if(age>18){
    console.log("you can vote"); //true -condition true
}
else{
    console.log("you canNOT vote"); //false
}

//-----------------------------------
let ageX =3;

ageX>18 ? console.log("you can vote") : console.log("you canNOT vote");

// or
ageX>18 ? "you can vote" : "you canNOT vote";
